-- Задача: найти дублирующиеся email
-- Зачем в тестировании: на один email не должно быть двух аккаунтов. Если запрос
-- что-то вернул — регистрация пускает дубли, это баг. В наших данных так и есть:
-- danila@test.com сидит сразу на двух юзерах (Данила и Мария).

SELECT email, COUNT(*) AS count
FROM users
GROUP BY email
HAVING COUNT(*) > 1;
